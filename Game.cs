        public static int Moves(char key)
        {
            int damage = 0;

            Random rand = new Random();
            switch (key)
            {
                case 'U':
                        damage = rand.Next(9);
                        break;
                case 'R':
                        damage = rand.Next(13);
                        break;
                case 'S':
                    
                        damage = rand.Next(21);
                        break;
                default:
                        damage = 0;
                        break;
                    
            }
            return damage;
        }


