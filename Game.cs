        public static int playersTurn()
        {
            Console.WriteLine("Press U to uppercut, R to Roundhouse or S to Sword Slash");
            char keyInput = Char.ToUpper(Console.ReadKey().KeyChar);
            int damage = Moves(keyInput);

            return damage;
